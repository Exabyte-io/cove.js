function rotateIcon(IconComponent: typeof SvgIcon, degrees: number) {
    function RotatedIcon(props: IconByNameProps) {
        const { sx } = props;

        return <IconComponent {...props} sx={{ transform: `rotate(${degrees}deg)`, ...sx }} />;
    }

    return RotatedIcon;
}

